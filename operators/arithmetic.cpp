#include <iostream>
using namespace std;

int main() {
    // Declare two integer variables
    int a = 10;
    int b = 5;

    // Addition
    cout << "a + b = " << (a + b) << endl;

    // Subtraction
    cout << "a - b = " << (a - b) << endl;

    // Multiplication
    cout << "a * b = " << (a * b) << endl;

    // Division
    cout << "a / b = " << (a / b) << endl;

    // Modulus
    cout << "a % b = " << (a % b) << endl;

    return 0;
}
